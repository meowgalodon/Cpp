#include <bits/stdc++.h>

using namespace std;

//Tinh + - * / cho 2 so nguyen bat ky
int main(){
	
	//Khai bao 2 so nguyen
	float a, b;
	cin >> a >> b;
	
	//Phep +
	cout << a << " + " << b << " = " << a + b << endl;
	
	//Phep -
	cout << a << " - " << b << " = " << a - b << endl;
	
	//Phep *
	cout << a << " * " << b << " = " << a * b << endl;
	
	//Phep /
	cout << a << " / " << b << " = " << a / b << endl;
	return 0;
}
